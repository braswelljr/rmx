package config

// Config - general configuration for the app is placed here.
type Config struct {
	// Version - the version of the app
	Version string
	// Name - the name of the app
	Name string
	// Author - the author of the app
	Author string
	// Email - the email of the author
	Email string
	// License - the license of the app
	License string
	// Description - the description of the app
	Description string
	// Usage - the usage of the app
	Usage string
	// Help - the help of the app
	Help string
	// Flags - the flags of the app
	Flags string
	// Commands - the commands of the app
	Commands string
	// Arguments - the arguments of the app
	Arguments string
}
